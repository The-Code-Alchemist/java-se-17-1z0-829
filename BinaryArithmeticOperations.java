public class BinaryArithmeticOperations {
    public static void main(String[] args) {
        int price = 2 * 5 + 3 * 4 - 8;  //  10 + 12 - 8 = 14
        System.out.println(price);      //  14

        price = 2 * ((5 + 3) * 4 - 8);  // 2 * (8 * 4 - 8) = 2 * (32 - 8) = 2 * 24 = 48
        System.out.println(price);

    }   // page 73
}
