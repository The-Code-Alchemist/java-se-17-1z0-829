public class PreAndPostfix {
    public static void main(String[] args) {
        int parkAtendance = 0;

        System.out.println(parkAtendance);      // 0
        System.out.println(++parkAtendance);    // 1
        System.out.println(parkAtendance);      // 1
        System.out.println(parkAtendance--);    // 1
        System.out.println(parkAtendance);      // 0
    }   // pages 71 and 72
}
